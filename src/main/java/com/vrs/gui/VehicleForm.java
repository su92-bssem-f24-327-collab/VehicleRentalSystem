    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      // TODO add your handling code here:
      String id = jTextField4.getText().trim();

    if(id.isEmpty()){
        JOptionPane.showMessageDialog(this, "Enter Vehicle ID to delete");
        return;
    }

    try {
        Connection con = DBConnection.getConnection();
        
        // Check if vehicle has rentals
        String checkSql = "SELECT COUNT(*) FROM rentals WHERE vehicle_id = ?";
        PreparedStatement checkPst = con.prepareStatement(checkSql);
        checkPst.setInt(1, Integer.parseInt(id));
        ResultSet rs = checkPst.executeQuery();
        rs.next();
        int rentalCount = rs.getInt(1);
        
        if(rentalCount > 0) {
            int confirm = JOptionPane.showConfirmDialog(this, 
                "This vehicle has " + rentalCount + " rental record(s). Delete anyway?", 
                "Warning", JOptionPane.YES_NO_OPTION);
            
            if(confirm == JOptionPane.YES_OPTION) {
                PreparedStatement deleteRentalsPst = con.prepareStatement("DELETE FROM rentals WHERE vehicle_id = ?");
                deleteRentalsPst.setInt(1, Integer.parseInt(id));
                deleteRentalsPst.executeUpdate();
                
                PreparedStatement deleteVehiclePst = con.prepareStatement("DELETE FROM vehicle WHERE id = ?");
                deleteVehiclePst.setInt(1, Integer.parseInt(id));
                deleteVehiclePst.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "Vehicle and rental history deleted!");
                clearFields();
                loadVehicleData();
            }
        } else {
            PreparedStatement deletePst = con.prepareStatement("DELETE FROM vehicle WHERE id=?");
            deletePst.setInt(1, Integer.parseInt(id));
            
            if(deletePst.executeUpdate() > 0) {
                JOptionPane.showMessageDialog(this, "Vehicle Deleted Successfully!");
                clearFields();
                loadVehicleData();
            } else {
                JOptionPane.showMessageDialog(this, "Vehicle ID not found!");
            }
        }
        con.close();

    } catch(Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_jButton2ActionPerformed

